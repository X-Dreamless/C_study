/*
	百分制，去掉一个最高分和一个最低分，输出每组的平均成绩

	一行，输入7个整数(0-100)，用空格分隔

	一行，输出去掉最高分和最低分的平均成绩，小数点后保留2位，每行输出后换行

	输入：99 45 78 67 72 88 60
	输出：73.00
*/
#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main()
{
	int 总和 = 0;
	int 成绩 = 0;
	int 最大 = 0;
	int 最小 = 100;

	printf("开始输入ctrl+c停止>");

	int i = 0;
	for (i = 0; i < 7; i++)
	{
		scanf("%d", &成绩);
		总和 += 成绩;

		if (成绩 > 最大)
		{
			最大 = 成绩;
		}
		if (成绩 < 最小)
		{
			最小 = 成绩;
		}
	}

	printf("%.2f\n", (总和 - 最大 - 最小) / 5.0);

	return 0;
}