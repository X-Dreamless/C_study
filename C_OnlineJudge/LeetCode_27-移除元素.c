#include <stdio.h>

int removeElement(int* nums, int numsSize, int val)
{

	/*
		思路1：
			输入：nums = [3,2,2,3], val = 3
			输出：2, nums = [2,2]

			遍历，如果元素等于val
				 ↓
				[3][2][2][3]

			后面的数据向前移动一位
				 ↓
				[2][2][3][ ]

			如果元素不等于val，val向后走一位
					↓
				[2][2][3][ ]

			时间复杂度O(N^2)
			最坏情况：数组中大部分或全部元素等于val


		思路2：
			输入：nums = [3,2,2,3], val = 3
			输出：2, nums = [2,2]

			创建 临时 数组
				[ ][ ][ ][ ]

			遍历nums数组
				 ↓
				[3][2][2][3]

			把不是val的值放到 临时 数组
				[3][2][2][3]
				↓
				[2][2][ ][ ]

			再把 临时 数组的值拷贝回去
				[2][2][ ][ ]
				↓
				[2][2][ ][ ]

			时间复杂度O(2N)
			空间复杂度O(N)

			空间换时间


		思路3：
			输入：nums = [3,2,2,3], val = 3
			输出：2, nums = [2,2]

			时间复杂度 优化到O(N)
			空间复杂度 优化到O(1)

			双指针

			定义两个变量，src 和 dst

			src找nums数组中不等于val的值，放到dst指向的位置
				src→
				[3][2][2][3]
				dst

			等于val，src++
				   src→
				[3][2][2][3]
				dst

			不等于val，放到dst指向的位置
				   src→
				[2][2][2][3]
				dst

			再src++，dst++
					  src→
				[2][2][2][3]
				   dst
	*/

	int src = 0;
	int dst = 0;

	while (src < numsSize)
	{
		if (nums[src] != val)
		{
			nums[dst] = nums[src];
			src++;
			dst++;
		}
		else
		{
			src++;
		}
	}

	return dst;
}

int main()
{
	int arr[] = { 3,2,2,3 };
	int sz = sizeof(arr) / sizeof(arr[0]);
	int ret = removeElement(arr, sz, 3);
	for (int i = 0; i < ret; i++)
	{
		printf("%d ", arr[i]);
	}

	return 0;
}