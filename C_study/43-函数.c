// 修改 指针变量
void 交换int(int* x, int* y) // 传址调用
{
	int z = 0;
	z = *x;
	*x = *y;
	*y = z;

	// 只需要交换就行 不需要返回
}

int main()
{
	// 找出两个整数中的最大值
	int a = 10;
	int b = 20;
	// 函数调用
	int max = 获取int最大(a, b);
	printf("%d\n", max);


	//
	// 交换两个整形变量的值
	int c = 11;// 4个字节空间
	int d = 22;// 

	printf("交换前:c = %d d = %d\n", c, d);
	// 需要修改值的时候才需要传地址
	交换int(&c, &d);
	printf("交换后:c = %d d = %d\n", c, d);

	return 0;
}