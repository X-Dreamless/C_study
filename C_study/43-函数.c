// 找出两个整数中的最大值
int 获取int最大(int x, int y)
{
	int z = 0;
	if (x > y)
	{
		z = x;
	}
	else
	{
		z = y;
	}

	// 返回z 较大值
	return z;
}




// 交换两个整形变量的值
// 只需要交换就行 不需要返回 所以返回类型是void(无效的)
// 函数返回类型void表示这个函数不返回任何值 也不需要返回
// 以下代码有问题，c和d传参数进去，x和y接收，x，y，z开辟了新内存空间，操作后不会影响a和b
// 函数在被调用的时候，实参传给形参 其实形参是实参的一份临时拷贝 改变形参 不能改变实参
//void 交换int(int x, int y) // 传值调用
